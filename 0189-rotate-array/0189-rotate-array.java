class Solution {
    public void rotate(int[] nums, int k) {
        // Reverse the whole array, then reverse the first k elements and the rest.
        int n = nums.length;
        if (n == 0) return;
        k = k % n;
        if (k < 0) k += n;
        reverse(nums, 0, n - 1);
        reverse(nums, 0, k - 1);
        reverse(nums, k, n - 1);
    }

    private void reverse(int[] arr, int left, int right) {
        while (left < right) {
            int tmp = arr[left];
            arr[left] = arr[right];
            arr[right] = tmp;
            left++;
            right--;
        }
    }
}

